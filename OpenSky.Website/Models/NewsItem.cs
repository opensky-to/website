namespace OpenSky.Website.Models
{
    /// -------------------------------------------------------------------------------------------------
    /// <summary>
    /// News item model (from news.json)
    /// </summary>
    /// <remarks>
    /// sushi.at, 19/05/2021.
    /// </remarks>
    /// -------------------------------------------------------------------------------------------------
    public class NewsItem
    {
        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets a value indicating whether the news item is active.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public bool Active { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the main heading.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public string H1 { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the sub heading.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public string H2 { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the image.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public string Img { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the paragraph.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public string P { get; set; }
    }
}