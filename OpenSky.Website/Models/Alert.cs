namespace OpenSky.Website.Models
{
    /// -------------------------------------------------------------------------------------------------
    /// <summary>
    /// Alert model.
    /// </summary>
    /// <remarks>
    /// sushi.at, 06/05/2021.
    /// </remarks>
    /// -------------------------------------------------------------------------------------------------
    public class Alert
    {
        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets a value indicating whether to automatically close the alert.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public bool AutoClose { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets a value indicating whether to fade the alert.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public bool Fade { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the identifier.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public string Id { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets a value indicating whether the keep the alert after a route change.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public bool KeepAfterRouteChange { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the message.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public string Message { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the type.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public AlertType Type { get; set; }
    }
}